let COUNTRY_SPLASH_NAME = 'basic_country_splash'
class CountrySplashSettings {
  static getBrand() {
    var brandElement,
      brand = document
        .querySelector('[data-countrysplash-brand]')
        .getAttribute('data-countrysplash-brand')
    return window.brandsJson[brand.toLowerCase()]
  }
  static getCookie() {
    var cookieArr = document.cookie.split(';')
    for (let i = 0; i < cookieArr.length; i++) {
      var cookie = cookieArr[i].trim()
      if (cookie.startsWith(COUNTRY_SPLASH_NAME + '=')) {
        var cookie = cookie.substring(COUNTRY_SPLASH_NAME.length + 1)
        return decodeURIComponent(cookie)
      }
    }
    return false
  }
  static setCookie(value) {
    var date = new Date(),
      date =
        (date.setTime(date.getTime() + 15552e6),
        'expires=' + date.toUTCString()),
      value = encodeURIComponent(value)
    document.cookie = COUNTRY_SPLASH_NAME + `=${value};${date};path=/`
  }
  static deleteCookie() {
    document.cookie =
      COUNTRY_SPLASH_NAME + '=; expires=Thu, 01 Jan 1970 00:00:00 UTC; path=/;'
  }
  static async getCountry() {
    var url =
      'https://api.basic.net/BasicBrandsCommon/anonymous/country-popup?domain=kappa.com'
    try {
      var response = await fetch(url),
        data
      let countryCode = (response.ok ? await response.json() : null)?.userInfo
        ?.countryIsoCode3
      return (
        window.countryJson?.find(
          (country) => country.codeThree === countryCode
        ) ||
        window.countryJson?.[0] ||
        false
      )
    } catch {
      return window.countryJson?.[0] || false
    }
  }
  static getUrlParameter() {
    var urlParams, parameterValue
    return new URLSearchParams(window.location.search).get(COUNTRY_SPLASH_NAME)
      ? true
      : false
  }
  static insertParameterInLink(link) {
    var link = new URL(link)
    link.searchParams.set(COUNTRY_SPLASH_NAME, 'remain')
    return link.toString()
  }
  static init() {
    window.basicCountrySplash = window.basicCountrySplash || {}
    window.basicCountrySplash.brand = this.getBrand()
    window.basicCountrySplash.urlParameter = this.getUrlParameter()
    window.basicCountrySplash.cookie = this.getCookie()
  }
}
class CountrySplashWrapper {
  static cookieManagment(cookie) {
    if ('remain' == cookie);
    else
      try {
        var urlCookie, cookieDomain, attualDomain
        if (new URL(cookie).hostname != window.location.hostname)
          window.location.href =
            CountrySplashSettings.insertParameterInLink(cookie).toString()
      } catch (error) {
        CountrySplashSettings.deleteCookie()
      }
  }
  static async verifyIp() {
    window.basicCountrySplash = window.basicCountrySplash || {}
    HTMLCountrySplash.injectLoader()
    window.basicCountrySplash.country = await CountrySplashSettings.getCountry()
    var urlDefault =
        window.basicCountrySplash.country[window.basicCountrySplash.brand.name],
      urlApi,
      apiDomain,
      attualDomain
    try {
      if (new URL(urlDefault).hostname != window.location.hostname) return false
      CountrySplashSettings.setCookie('remain')
      HTMLCountrySplash.removeCountryPopUp()
      return true
    } catch (error) {
      CountrySplashSettings.deleteCookie()
      HTMLCountrySplash.removeCountryPopUp()
      return false
    }
  }
  static async fastOpen() {
    window.basicCountrySplash.country = await CountrySplashSettings.getCountry()
    HTMLCountrySplash.injectCountryPopUp()
  }
  static async openFromFooter() {
    document.addEventListener('DOMContentLoaded', () => {
      var elements
      document.querySelectorAll('.countrySplash-open').forEach((element) => {
        element.addEventListener('click', () => {
          this.fastOpen()
        })
      })
    })
  }
  static isCrawler() {
    function containsAny(mainString, stringArray) {
      return stringArray.some((subString) => mainString.includes(subString))
    }
    var userAgent, crawlers
    return containsAny(navigator.userAgent.toLowerCase(), [
      'googlebot',
      'adsbot-google-mobile',
      'bingbot',
    ])
  }
  static async init() {
    var isBot, isPreview, verifyIp
    if (this.isCrawler());
    else {
      if (window.location.href.includes('shopifypreview'));
      else {
        CountrySplashSettings.init()
        await this.openFromFooter()
        if (!window.basicCountrySplash.brand)
          CountrySplashSettings.deleteCookie()
        else if (window.basicCountrySplash.urlParameter)
          CountrySplashSettings.setCookie('remain')
        else if (window.basicCountrySplash.cookie)
          this.cookieManagment(window.basicCountrySplash.cookie)
        else {
          if (await this.verifyIp());
          else HTMLCountrySplash.injectCountryPopUp()
        }
      }
    }
  }
}
